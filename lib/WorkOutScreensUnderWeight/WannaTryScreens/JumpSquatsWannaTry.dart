class JumpSquatsWannaTry extends StatefulWidget {
  const JumpSquatsWannaTry({Key? key}) : super(key: key);

  @override
  State<JumpSquatsWannaTry> createState() => _JumpSquatsWannaTry();
}

class _JumpSquatsWannaTry extends State<JumpSquatsWannaTry>
    with TickerProviderStateMixin {
